        //     User::where('email', $request->input('email'))->update(['api_key' => "$apikey"]);
        //     return response()->json(['status' => 'success', 'api_key' => $apikey]);
        // } else {
        //     return response()->json(['status' => 'fail'], 401);
        // }
    }
}
